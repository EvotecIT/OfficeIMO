namespace OfficeIMO.Markdown;

/// <summary>Built-in Prism themes.</summary>
public enum PrismTheme {
    /// <summary>Default light theme (prism.min.css).</summary>
    Prism,
    /// <summary>Okaidia dark theme (prism-okaidia.min.css).</summary>
    Okaidia,
    /// <summary>Dark theme aligned with GitHub-like dark (maps to Okaidia).</summary>
    GithubDark,
    /// <summary>Auto: light in light mode (Prism), dark in dark mode (Okaidia).</summary>
    GithubAuto
}

