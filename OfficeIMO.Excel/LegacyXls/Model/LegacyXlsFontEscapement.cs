namespace OfficeIMO.Excel.LegacyXls.Model {
    /// <summary>
    /// Identifies legacy XLS font escapement.
    /// </summary>
    public enum LegacyXlsFontEscapement {
        /// <summary>
        /// Text uses the baseline position.
        /// </summary>
        None,

        /// <summary>
        /// Text is superscript.
        /// </summary>
        Superscript,

        /// <summary>
        /// Text is subscript.
        /// </summary>
        Subscript
    }
}
