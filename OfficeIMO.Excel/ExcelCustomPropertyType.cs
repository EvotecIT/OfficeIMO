namespace OfficeIMO.Excel {
    /// <summary>
    /// Supported custom workbook property value types.
    /// </summary>
    public enum ExcelCustomPropertyType {
        /// <summary>
        /// Text value.
        /// </summary>
        Text,

        /// <summary>
        /// Integer value.
        /// </summary>
        NumberInteger,

        /// <summary>
        /// Floating point numeric value.
        /// </summary>
        NumberDouble,

        /// <summary>
        /// Boolean value.
        /// </summary>
        YesNo,

        /// <summary>
        /// Date/time value.
        /// </summary>
        DateTime
    }
}
