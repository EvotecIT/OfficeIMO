namespace OfficeIMO.PowerPoint {
    /// <summary>
    ///     Controls how text is auto-fitted inside a text box.
    /// </summary>
    public enum PowerPointTextAutoFit {
        /// <summary>
        ///     Explicitly disable auto-fit.
        /// </summary>
        None,
        /// <summary>
        ///     Shrink text to fit the shape.
        /// </summary>
        Normal,
        /// <summary>
        ///     Resize the shape to fit text.
        /// </summary>
        Shape
    }
}
