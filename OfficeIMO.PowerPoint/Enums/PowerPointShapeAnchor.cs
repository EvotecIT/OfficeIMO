namespace OfficeIMO.PowerPoint {
    /// <summary>
    ///     Anchor positions used when resizing or scaling shapes.
    /// </summary>
    public enum PowerPointShapeAnchor {
        /// <summary>
        ///     Top-left corner.
        /// </summary>
        TopLeft,
        /// <summary>
        ///     Top edge center.
        /// </summary>
        Top,
        /// <summary>
        ///     Top-right corner.
        /// </summary>
        TopRight,
        /// <summary>
        ///     Left edge center.
        /// </summary>
        Left,
        /// <summary>
        ///     Center point.
        /// </summary>
        Center,
        /// <summary>
        ///     Right edge center.
        /// </summary>
        Right,
        /// <summary>
        ///     Bottom-left corner.
        /// </summary>
        BottomLeft,
        /// <summary>
        ///     Bottom edge center.
        /// </summary>
        Bottom,
        /// <summary>
        ///     Bottom-right corner.
        /// </summary>
        BottomRight
    }
}
