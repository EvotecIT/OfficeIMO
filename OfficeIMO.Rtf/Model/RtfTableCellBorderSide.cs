    Top,

    /// <summary>Left border.</summary>
    Left,

    /// <summary>Bottom border.</summary>
    Bottom,

    /// <summary>Right border.</summary>
    Right,

    /// <summary>Diagonal border from top-left to bottom-right.</summary>
    TopLeftToBottomRight,

    /// <summary>Diagonal border from top-right to bottom-left.</summary>
    TopRightToBottomLeft
}
