namespace OfficeIMO.Rtf;

/// <summary>
/// Named RTF shading pattern controls.
/// </summary>
public enum RtfShadingPattern {
    /// <summary>No named shading pattern.</summary>
    None,

    /// <summary>Horizontal line pattern.</summary>
    Horizontal,

    /// <summary>Vertical line pattern.</summary>
    Vertical,

    /// <summary>Forward diagonal line pattern.</summary>
    ForwardDiagonal,

    /// <summary>Backward diagonal line pattern.</summary>
    BackwardDiagonal,

    /// <summary>Horizontal and vertical cross pattern.</summary>
    Cross,

    /// <summary>Diagonal cross pattern.</summary>
    DiagonalCross,

    /// <summary>Dark horizontal line pattern.</summary>
    DarkHorizontal,

    /// <summary>Dark vertical line pattern.</summary>
    DarkVertical,

    /// <summary>Dark forward diagonal line pattern.</summary>
    DarkForwardDiagonal,

    /// <summary>Dark backward diagonal line pattern.</summary>
    DarkBackwardDiagonal,

    /// <summary>Dark horizontal and vertical cross pattern.</summary>
    DarkCross,

    /// <summary>Dark diagonal cross pattern.</summary>
    DarkDiagonalCross
}
