namespace OfficeIMO.Rtf;

/// <summary>
/// Defines vertical text alignment for content on an RTF section page.
/// </summary>
public enum RtfSectionVerticalAlignment {
    /// <summary>Align section text to the top of the page.</summary>
    Top,

    /// <summary>Center section text vertically on the page.</summary>
    Center,

    /// <summary>Align section text to the bottom of the page.</summary>
    Bottom,

    /// <summary>Justify section text vertically on the page.</summary>
    Justified
}
