namespace OfficeIMO.Drawing;

/// <summary>
/// Horizontal alignment for reusable OfficeIMO drawing text.
/// </summary>
public enum OfficeTextAlignment {
    /// <summary>Align text to the left edge of the text box.</summary>
    Left,

    /// <summary>Center text inside the text box.</summary>
    Center,

    /// <summary>Align text to the right edge of the text box.</summary>
    Right,

    /// <summary>Distribute words across the available line width when supported.</summary>
    Justify
}
