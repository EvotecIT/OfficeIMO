namespace OfficeIMO.Pdf;

/// <summary>
/// Built-in vector icon shapes that can be drawn inside table cells.
/// </summary>
public enum PdfCellIconKind {
    /// <summary>A filled circle.</summary>
    Circle,
    /// <summary>A filled diamond.</summary>
    Diamond,
    /// <summary>A filled square.</summary>
    Square,
    /// <summary>A filled upward triangle.</summary>
    TriangleUp,
    /// <summary>A filled right-pointing triangle.</summary>
    TriangleRight,
    /// <summary>A filled downward triangle.</summary>
    TriangleDown,
    /// <summary>An outlined square checkbox.</summary>
    CheckBoxUnchecked,
    /// <summary>An outlined square checkbox with a check mark.</summary>
    CheckBoxChecked
}
