namespace OfficeIMO.Pdf;

/// <summary>
/// Vertical alignment used by fixed-height PDF content boxes.
/// </summary>
public enum PdfVerticalAlign {
    /// <summary>Align content to the top of the box.</summary>
    Top,
    /// <summary>Align content vertically centered in the box.</summary>
    Middle,
    /// <summary>Align content to the bottom of the box.</summary>
    Bottom
}
