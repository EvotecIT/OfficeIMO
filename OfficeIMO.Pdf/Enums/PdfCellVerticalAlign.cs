namespace OfficeIMO.Pdf;

/// <summary>
/// Vertical text alignment used by table cell rendering.
/// </summary>
public enum PdfCellVerticalAlign {
    /// <summary>Align text to the top of the cell.</summary>
    Top,
    /// <summary>Align text vertically centered in the cell.</summary>
    Middle,
    /// <summary>Align text to the bottom of the cell.</summary>
    Bottom
}
