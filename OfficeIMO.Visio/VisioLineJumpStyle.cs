namespace OfficeIMO.Visio {
    /// <summary>
    /// Specifies the line jump style used by connectors on a Visio page.
    /// </summary>
    public enum VisioLineJumpStyle {
        /// <summary>Use Visio's default arc line jump.</summary>
        Default = 0,

        /// <summary>Use an arc line jump.</summary>
        Arc = 1,

        /// <summary>Use a gap line jump.</summary>
        Gap = 2,

        /// <summary>Use a square line jump.</summary>
        Square = 3,

        /// <summary>Use a two-sided line jump.</summary>
        TwoSides = 4,

        /// <summary>Use a three-sided line jump.</summary>
        ThreeSides = 5,

        /// <summary>Use a four-sided line jump.</summary>
        FourSides = 6,

        /// <summary>Use a five-sided line jump.</summary>
        FiveSides = 7,

        /// <summary>Use a six-sided line jump.</summary>
        SixSides = 8,

        /// <summary>Use a seven-sided line jump.</summary>
        SevenSides = 9
    }
}
