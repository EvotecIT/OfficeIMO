namespace OfficeIMO.Visio {
    /// <summary>
    /// Horizontal text alignment inside a Visio shape.
    /// </summary>
    public enum VisioTextHorizontalAlignment {
        /// <summary>Align text to the left edge.</summary>
        Left = 0,

        /// <summary>Center text horizontally.</summary>
        Center = 1,

        /// <summary>Align text to the right edge.</summary>
        Right = 2,

        /// <summary>Justify text across the available width.</summary>
        Justify = 3
    }
}
