namespace OfficeIMO.Visio {
    /// <summary>
    /// Specifies whether a Visio page name appears in the Visio user interface.
    /// </summary>
    public enum VisioPageUiVisibility {
        /// <summary>
        /// Display the page name in page tabs and other Visio UI surfaces.
        /// </summary>
        Normal = 0,

        /// <summary>
        /// Hide the page name from Visio UI surfaces that list pages by name.
        /// </summary>
        Hidden = 1
    }
}
