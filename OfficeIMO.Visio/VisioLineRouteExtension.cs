namespace OfficeIMO.Visio {
    /// <summary>
    /// Specifies the default appearance of routed connectors on a Visio page.
    /// </summary>
    public enum VisioLineRouteExtension {
        /// <summary>Use Visio's default straight connector appearance.</summary>
        Default = 0,

        /// <summary>Use straight connectors.</summary>
        Straight = 1,

        /// <summary>Use curved connectors.</summary>
        Curved = 2
    }
}
