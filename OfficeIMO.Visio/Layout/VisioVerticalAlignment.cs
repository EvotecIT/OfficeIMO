namespace OfficeIMO.Visio {
    /// <summary>
    /// Vertical alignment target for selected Visio shapes.
    /// </summary>
    public enum VisioVerticalAlignment {
        /// <summary>Align selected shapes to the bottom edge of the selection bounds.</summary>
        Bottom,

        /// <summary>Align selected shapes to the vertical middle of the selection bounds.</summary>
        Middle,

        /// <summary>Align selected shapes to the top edge of the selection bounds.</summary>
        Top
    }
}
