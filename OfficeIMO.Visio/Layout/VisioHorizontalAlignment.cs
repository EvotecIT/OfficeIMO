namespace OfficeIMO.Visio {
    /// <summary>
    /// Horizontal alignment target for selected Visio shapes.
    /// </summary>
    public enum VisioHorizontalAlignment {
        /// <summary>Align selected shapes to the left edge of the selection bounds.</summary>
        Left,

        /// <summary>Align selected shapes to the horizontal center of the selection bounds.</summary>
        Center,

        /// <summary>Align selected shapes to the right edge of the selection bounds.</summary>
        Right
    }
}
